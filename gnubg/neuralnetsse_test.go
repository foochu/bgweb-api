func _openFile(filename string) *os.File {
	f, err := os.OpenFile(filename, os.O_RDONLY, 0644)
	if err != nil {
		panic(err)
	}
	if err = verifyWeights(f, filename); err != nil {
		panic(err)
	}
	return f
}

